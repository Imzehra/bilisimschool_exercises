import turtle 

t = turtle.Turtle(shape="turtle")
t.pensize(10)
t.circle(50)

t.penup()
t.setposition (120,0)
t.pendown()
t.color("red")
t.circle(50)

t.penup()
t.setposition (-120,0)
t.pendown()
t.color("blue")
t.circle(50)

t.penup()
t.setposition (60,-60)
t.pendown()
t.color("green")
t.circle(50)

t.penup()
t.setposition (-60,-60)
t.pendown()
t.color("yellow")
t.circle(50)
